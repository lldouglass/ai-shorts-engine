ENGAGEMENT_STRATEGIES = f"""{VIDEO_STRUCTURE}

{COMMENT_STRATEGIES}

{CTR_STRATEGIES}

{CAPTION_STRATEGIES}

{VISUAL_FORMAT_STRATEGIES}"""


def get_strategies_for_niche(niche: str | None) -> str:
    """Get the combined engagement strategies plus niche-specific additions.

    Args:
        niche: Content niche (e.g., "cars", "stocks", "dark_stories")

    Returns:
        Combined strategy string for prompt injection
    """
    base = ENGAGEMENT_STRATEGIES

    if not niche:
        return base

    niche_lower = niche.lower()

    if any(term in niche_lower for term in ["car", "auto", "vehicle", "reliab"]):
        return f"{base}\n\n{CAR_NICHE_STRATEGIES}"
    elif any(term in niche_lower for term in ["stock", "invest", "moat", "finance", "buffett"]):
        return f"{base}\n\n{STOCK_NICHE_STRATEGIES}"
    elif any(term in niche_lower for term in ["dark", "thriller", "horror", "ai story", "sci-fi"]):
        return f"{base}\n\n{DARK_STORY_NICHE_STRATEGIES}"

    return base
