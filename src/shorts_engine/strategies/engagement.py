# VIDEO STRUCTURE FRAMEWORK
# =============================================================================

VIDEO_STRUCTURE = """## Video Structure (MANDATORY 4-Step Framework)
Every video MUST follow this structure:

1. HOOK (0-3 seconds) - Scroll-stopper that creates immediate curiosity
   - Bold visual or unexpected motion
   - Curiosity gap question or shocking stat
   - Text overlay: benefit-driven, max 8 words
   - Add instant credibility fast: sample size, proof point, or social proof
   - This determines EVERYTHING. 65% of viewers who make it past 3 seconds watch to 10s.

2. VALUE DROP (4-15 seconds) - Core message delivered fast
   - ONE idea per video, never more
   - Specific data/numbers beat vague claims
   - Keep pace fast, remove all fluff
   - Text overlays for silent viewers (90%+ watch without sound)

3. STORY/PAYOFF (16-45 seconds) - The meat, show don't tell
   - Reveal the answer, show the data, demonstrate the tool
   - Build tension with pacing variation and clear visual contrast
   - If it's a list: show 3-5 items, WITHHOLD the rest (website CTA)

4. CALL-TO-ACTION (final 3-5 seconds) - Natural next step, not salesy
   - Comment prompt OR website tease (pick one per video, never both)
   - Must feel like a conversation, not a pitch
   - The CTA provides ADDITIONAL value, not just asks for something"""


