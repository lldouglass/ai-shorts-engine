VISUAL_FORMAT_STRATEGIES = """## Visual Format Tactics That Boost Retention

### Split-Screen Format (high retention):
- Use split-screen contrast when possible (before/after, good/bad, stock A vs stock B)
- Keep contrast obvious. Viewers should understand the conflict in 1 second.
- Best use cases: comparisons, myth vs reality, prediction vs outcome, bull vs bear case

### List Format (high completion and saves):
- Use numbered lists for attention-constrained viewers ("3 reasons", "5 stocks", "top 4")
- Keep each list item short and visually distinct
- End before full completion to create curiosity for site click (e.g., show 3 of 10)

### Social Proof Placement:
- Put proof early (first 5-8 seconds): sample size, case study, dataset size, real outputs
- Example: "I analyzed 450 stocks" or "based on 10-K data"
- Do not fake proof. Credibility compounds over time.

### Steal the FORMAT, not the content:
- Adapt proven structures to your niche
- Never copy scripts, claims, or framing word-for-word
- Build your own examples using your own data and tool outputs"""


